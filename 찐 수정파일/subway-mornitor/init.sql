CREATE TABLE alerts (
    id INT PRIMARY KEY AUTO_INCREMENT,
    device_id VARCHAR(50),
    alert_type VARCHAR(50),
    message TEXT,
    detected_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (device_id) REFERENCES devices(id)
);

